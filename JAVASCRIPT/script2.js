// Function Declaration
function a1(a, b) {
    return a + b;
}

console.log(a1(2, 3));

//function expression

const addition = function (a, b) {
    return a + b;
}

console.log(addition(5, 6));

//Arror Function

const add = (a, b) => a + b;


console.log(add(10, 34));

//Arrow functiom with No Parameters

const greet = () => console.log("Welcome")

greet();

//With One Parameter

const greet1 = (x) => x * x;
console.log(greet1(10));

//With Multiple Parameters

const greet2 = (a, b, c) => a + b * c;
console.log(greet2(10, 20, 30))
















































































































































































































































































